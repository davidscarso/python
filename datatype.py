
#String str
print(type("Hello world"))

#Integet int
print(type(1))

#Float float
print(type(1.2))

#Boolean bool
print(type(True))
print(type(False))

#List list
print(type([12,20,30]))

#Tuples (Es como enumerado)
print(type((12,20,30)))

#Dictionaries dict
print(type({
    "name ":"Fulanito",
    "lastname":"Cosmes",
    "nickname":"Pepo"
}))

#NoneType
print(type(None))